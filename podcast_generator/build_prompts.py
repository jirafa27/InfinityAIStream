from podcast_generator.topic_rules import format_banned_topics


class PromptsBuilder:
    """Класс для построения промптов"""

    def build_prompt_for_monologue(self, recent_topics: list[str], current_topic: str) -> str:
        banned = format_banned_topics(recent_topics)
        return (
            f"Ты — подкастер и ведешь бесконечный философский стендап со смешными шутками.\n"
            f"В каждом панчлайне — слом ожиданий.\n\n"
            f"Запрещённые темы (уже были, нельзя повторять и нельзя делать почти такие же):\n"
            f"{banned}\n\n"
            f"Напиши монолог СТРОГО на тему «{current_topic}».\n"
            f"Весь текст только про эту тему — не переключайся на другие сюжеты и предметы.\n\n"
            f"Начинай сразу с сути — без приветствий и вступлений. "
            f"Запрещено: «привет», «друзья», «добрый день/вечер», «сегодня поговорим», "
            f"«давайте начнём», обращения к аудитории в начале.\n\n"
            f"900–1100 символов, строго не больше 1100. "
            f"Один связный текст, без списков. "
            f"Не говори «в прошлый раз», не отсылайся к прошлым темам. "
            f"Не пиши «НОВАЯ ТЕМА» и не придумывай тему для следующего выпуска. "
            f"Без markdown. Только текст монолога."
        )

    def build_prompt_for_fresh_topic(
        self, current_topic: str, recent_topics: list[str]
    ) -> str:
        banned = format_banned_topics(recent_topics)
        return (
            f"Текущая тема монолога: {current_topic}\n"
            f"Запрещённые темы (нельзя повторять):\n{banned}\n\n"
            f"Придумай одну новую оригинальную тему для следующего монолога. "
            f"Ответь строго одной строкой: НОВАЯ ТЕМА: название"
        )

    def build_prompt_for_comment(self, current_topic, content, author):
        return (
            f"Ты ведешь философский стендап. В чате написали: «{content}» от пользователя {author}.\n\n"
            f"Ответь коротко, остро и с юмором — 1–2 предложения, максимум 180 символов. "
            f"Обратись по нику ({author}). Без пересказа всей фразы зрителя.\n\n"
            f"Если оскорбление — короткий жёсткий подкол.\n"
            f"Тема эфира: {current_topic}.\n\n"
            f"Только текст ответа, без markdown."
        )
